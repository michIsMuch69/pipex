#include "../hdr/libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	void	*tmp;

	tmp = s;
	while (n-- > 0)
	{
		*(char *)s = (char) c;
		s++;
	}
	return (tmp);
}
